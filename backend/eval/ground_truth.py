from __future__ import annotations

import csv
import json
import logging
from collections.abc import Iterator
from dataclasses import dataclass, field
from pathlib import Path

logger = logging.getLogger(__name__)

DATA_DIR = Path(__file__).parent.parent / "data"


@dataclass
class GroundTruth:
    """Labeled ground truth for one dataset."""

    # alert_id → GT incident label (e.g. "failure-001")
    cluster_labels: dict[str, str] = field(default_factory=dict)
    # GT incident label → root alert_id (or "|"-joined top-3 for hit@3)
    root_causes: dict[str, str] = field(default_factory=dict)
    # GT incident label → failure type ("db" | "network" | "deploy")
    failure_types: dict[str, str] = field(default_factory=dict)


# AIOps Challenge 2020 uses Chinese column names — mapped here explicitly
_AIOPS_COL = {
    "fault_start": ["故障时间(开始)", "start_time", "failure_start"],
    "fault_end": ["故障时间(结束)", "end_time", "failure_end"],
    "cmdb_id": ["故障机器", "cmdb_id", "host"],
    "fault_type": ["故障类型", "type", "failure_type"],
}


class GroundTruthLoader:
    """
    Loads labeled ground truth for evaluation datasets.

    Supported formats:
    - {dataset}_gt.json   (our canonical format — generated by scripts/parse_aiops.py)
    - {dataset}_gt.csv    (AIOps Challenge 2020 failure CSV with Chinese columns)
    """

    def load(self, dataset: str) -> GroundTruth:
        for subdir in ("samples", "raw"):
            path = DATA_DIR / subdir / f"{dataset}_gt.json"
            if path.exists():
                return self._load_json(path)
            path = DATA_DIR / subdir / f"{dataset}_gt.csv"
            if path.exists():
                return self._load_aiops_csv(path)
        logger.warning("No ground truth found for dataset '%s'", dataset)
        return GroundTruth()

    def load_raw_alerts(self, dataset: str) -> Iterator[dict]:
        """Yield raw alert dicts for a dataset (JSONL or CSV)."""
        for subdir in ("samples", "raw"):
            path = DATA_DIR / subdir / f"{dataset}.jsonl"
            if path.exists():
                with open(path, encoding="utf-8") as f:
                    for line in f:
                        line = line.strip()
                        if line:
                            try:
                                yield json.loads(line)
                            except json.JSONDecodeError:
                                continue
                return
            path = DATA_DIR / subdir / f"{dataset}.csv"
            if path.exists():
                with open(path, encoding="utf-8-sig") as f:
                    yield from csv.DictReader(f)
                return

    def _load_json(self, path: Path) -> GroundTruth:
        data = json.loads(path.read_text(encoding="utf-8"))
        return GroundTruth(
            cluster_labels=data.get("cluster_labels", {}),
            root_causes=data.get("root_causes", {}),
            failure_types=data.get("failure_types", {}),
        )

    def _load_aiops_csv(self, path: Path) -> GroundTruth:
        """
        Parse AIOps Challenge 2020 failure CSV.
        Column names are in Chinese — mapped via _AIOPS_COL.
        Alert-level assignments are done in scripts/parse_aiops.py which also
        generates the _gt.json file used above.
        """
        gt = GroundTruth()
        with open(path, encoding="utf-8-sig") as f:
            reader = csv.DictReader(f)
            for i, row in enumerate(reader):
                label = f"failure-{i:04d}"
                fault_type = self._get(row, "fault_type") or "unknown"
                gt.failure_types[label] = fault_type
        logger.info("Loaded AIOps GT CSV: %d failure records", len(gt.failure_types))
        return gt

    @staticmethod
    def _get(row: dict, field_name: str) -> str | None:
        for col in _AIOPS_COL.get(field_name, []):
            if col in row and row[col]:
                return row[col]
        return None
